use crossterm::style::Color;

#[repr(u8)]
#[derive(Clone, Copy, PartialEq)]
pub enum PieceType {
    None = 0,
    I = 1,
    O = 2,
    T = 3,
    J = 4,
    L = 5,
    S = 6,
    Z = 7,
}

type Grid = [[u8; 4]; 4];

pub fn get_piece_color(id: PieceType) -> Color {
    match id {
        PieceType::I => Color::Rgb {
            r: 0,
            g: 220,
            b: 220,
        },
        PieceType::O => Color::Rgb {
            r: 220,
            g: 220,
            b: 0,
        },
        PieceType::T => Color::Rgb {
            r: 220,
            g: 0,
            b: 220,
        },
        PieceType::J => Color::Rgb { r: 0, g: 0, b: 220 },
        PieceType::L => Color::Rgb {
            r: 220,
            g: 100,
            b: 0,
        },
        PieceType::S => Color::Rgb { r: 0, g: 220, b: 0 },
        PieceType::Z => Color::Rgb { r: 220, g: 0, b: 0 },
        _ => Color::Black,
    }
}

